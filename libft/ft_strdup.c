#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int 	i;
	int		len;

	i = 0;
	len = ft_strlen(s1);
	if ((s2 = (char *)malloc((sizeof(*s1) * len) + 1)) == NULL)
		return (0);
	while (i < len)
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
